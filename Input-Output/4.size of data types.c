//size of data types like int,float,double and char
#include<stdio.h>
int main()
{
    int a;
    float b;
    double c;
    char d;

    printf("size of int is %d bytes\n",sizeof(a));
    printf("size of float is %d bytes\n",sizeof(b));
    printf("size of double is %d bytes\n",sizeof(c));
    printf("size of character is %d bytes\n",sizeof(d));
}
